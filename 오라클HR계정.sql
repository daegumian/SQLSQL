--테이블 최신본 생성 (위에서부터 차례대로 추가하면 됨)

CREATE TABLE FILM (   --영화정보
    FILMNUM VARCHAR2(1000) CONSTRAINT FILM_PK PRIMARY KEY,
    FILMTITLE VARCHAR2(1000) NOT NULL,
    FILMCONTENT VARCHAR2(1000) ,
    FILMRUNTIME VARCHAR2(200) NOT NULL,
    FILMPRICE NUMBER(10) NOT NULL
);

CREATE TABLE HMEMBER(   --회원
    MEMID VARCHAR2(350) CONSTRAINT MEM_PK PRIMARY KEY,
    MEMPW VARCHAR2(350) NOT NULL,
    MEMNAME VARCHAR2(100) NOT NULL,
    MEMPHONE VARCHAR2(30)
);

CREATE TABLE THEATER(   --상영관정보
    THEANUM VARCHAR2(1000) CONSTRAINT THEA_PK PRIMARY KEY,
    THEASEAT NUMBER(10)
);


CREATE TABLE SCHEDULE(  --상영스케쥴   (기존의 LEFTSEAT 테이블삭제됨)
    SCHENUM VARCHAR2(1000) CONSTRAINT SCHE_PK PRIMARY KEY,
    SCHEDATE DATE,
    SCHETIME VARCHAR2(1000) ,
    THEANUM VARCHAR2(1000) CONSTRAINT SCHE_THEA_FK REFERENCES THEATER(THEANUM),
    FILMNUM VARCHAR2(1000) CONSTRAINT SCHE_FILM_FK REFERENCES FILM(FILMNUM)
);


CREATE TABLE SEAT (    --좌석 새로생긴테이블
    SERINUM  VARCHAR2(300) CONSTRAINT SEAT_SERINUM_PK PRIMARY KEY, 
    SEATNUM VARCHAR2(1000),
    THEANUM VARCHAR2(1000) CONSTRAINT SEAT_THEANUM_FK REFERENCES THEATER(THEANUM)
);

CREATE TABLE TICKETING(   --예매 (TICKSEAT 테이블 삭제,  FK였던  MEMID삭제, 다른 FK 생성됨 SEATNUM
    TICKNO VARCHAR2(1000) CONSTRAINT TICK_PK PRIMARY KEY,
    TICKDATE DATE,
    TICKCOUNT NUMBER(10),
    TICKPRICE NUMBER(20) NOT NULL,
    PAYMEHOD VARCHAR2(1000),
    SCHENUM VARCHAR2(1000) CONSTRAINT TICK_SCHE_FK REFERENCES SCHEDULE(SCHENUM)   
);

CREATE TABLE TICKETLIST (  --예매내역 새로생긴테이블
    TLSERIAL NUMBER(38) CONSTRAINT TICKETLIST_PK PRIMARY KEY,
    TICKNO VARCHAR2(1000) CONSTRAINT TICKETLIST_TICKET_FK REFERENCES TICKETING(TICKNO),
    MEMID VARCHAR2(1000) CONSTRAINT TICKETLIST_HMEMBER_FK REFERENCES HMEMBER(MEMID),
    SEATNUM VARCHAR2(1000)
);

